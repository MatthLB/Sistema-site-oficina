    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cpf = jFormattedTextField1.getText();
        String nome = jTextField2.getText();
        String endereco = jTextField3.getText();
        String telefone = jFormattedTextField2.getText();
        String cargo = (String) jComboBox1.getSelectedItem();
        Double salario = Double.parseDouble(jTextField1.getText());
        
        for (Object o : listaF) {
            Funcionario f = (Funcionario) o;

            if (cpf.equals(f.getCpf())) {
                f.setNome(nome);
                f.setEndereco(endereco);
                f.setTelefone(telefone);
                f.setCargo(cargo);
                f.setSalario(salario);

                this.updateTable();
                break;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = jTable1.getSelectedRow();

        if (index != -1) {
            listaF.remove(index);
            
            modelo.removeRow(index);
            modelo.fireTableRowsDeleted(index, index);         
        } else {
            JOptionPane.showMessageDialog(this, "Para remover um produto da lista, clique nele para selecioná-lo e então aperte o botão 'Remover'");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void updateTable() {
        modelo.setNumRows(0);
        for (Object o : listaF) {
            Funcionario p = (Funcionario) o;

            modelo.addRow(new Object[]{p.getCpf(), p.getNome(), p.getEndereco(), p.getTelefone(), p.getCargo(), p.getSalario()});
        }
    }

