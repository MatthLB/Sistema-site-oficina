    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cpf = jFormattedTextField1.getText();
        String nome = jTextField2.getText();
        String endereco = jTextField3.getText();
        String telefone = jFormattedTextField2.getText();
        String cargo = (String) jComboBox1.getSelectedItem();
        Double salario = Double.parseDouble(jTextField1.getText());
        String login = jTextField4.getText();
        String senha = jPasswordField1.getPassword().toString();
        String confirmar_senha = jPasswordField2.getPassword().toString();
        
        if (confirmar_senha.equals(senha)) {            
            JOptionPane.showMessageDialog(null, "Senhas diferentes!");
            return;
        }
        
        Funcionario fc = new Funcionario();
        fc.setCpf(cpf);
        fc.setNome(nome);
        fc.setEndereco(endereco);
        fc.setTelefone(telefone);
        fc.setCargo(cargo);
        fc.setSalario(salario);
        fc.setLogin(login);
        fc.setSenha(senha);
        
        listaF.add(fc);
        
        jFormattedTextField1.setText("");
        jFormattedTextField2.setText("");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jPasswordField1.setText("");
        jPasswordField1.setText("");
        
        this.updateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

