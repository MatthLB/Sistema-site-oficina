    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cnpj = jFormattedTextField1.getText();
        String razao = jTextField2.getText();
        String endereco = jTextField3.getText();
        String telefone = jFormattedTextField2.getText();
        
        Fornecedor fd = new Fornecedor();
        fd.setCnpj(cnpj);
        fd.setRazao(razao);
        fd.setEndereco(endereco);
        fd.setTelefone(telefone);
        
        listaF.add(fd);
        
        jFormattedTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jFormattedTextField2.setText("");
        
        this.updateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cnpj = jFormattedTextField1.getText();
        String razao = jTextField2.getText();
        String endereco = jTextField2.getText();
        String telefone = jFormattedTextField2.getText();
        
        for (Object o : listaF) {
            Fornecedor f = (Fornecedor) o;

            if (cnpj.equals(f.getCnpj())) {
                f.setEndereco(endereco);
                f.setTelefone(telefone);
                f.setTelefone(telefone);

                this.updateTable();
                break;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = jTable1.getSelectedRow();

        if (index != -1) {
            listaF.remove(index);
            
            modelo.removeRow(index);
            modelo.fireTableRowsDeleted(index, index);         
        } else {
            JOptionPane.showMessageDialog(this, "Para remover um produto da lista, clique nele para selecioná-lo e então aperte o botão 'Remover'");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

