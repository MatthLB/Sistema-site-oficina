    private void updateTable() {
        modelo.setNumRows(0);
        for (Object o : listaF) {
            Fornecedor c = (Fornecedor) o;

            modelo.addRow(new Object[]{c.getCnpj(), c.getRazao(), c.getEndereco(), c.getTelefone()});
        }
    }

