    // Finalizar Venda
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Vector<Vector> items = modelo.getDataVector();

        for (int i = 0; i < items.size(); i++) {
            Vector item = items.get(i);

            int codigo = (int) item.get(0);
            int quantidadeProduto = (int) item.get(3);

            // Dar baixa no estoque
            for (Object o : listaProdutos) {
                p = (Produto) o;

                if (p.getCodigo() == codigo) {
                    p.setQuantidadeEstoque(p.getQuantidadeEstoque() - quantidadeProduto);
                    break;
                }
            }
        }
        totalVenda = 0.0;
        jTextField5.setText(totalVenda.toString());

        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();

        JOptionPane.showMessageDialog(this, "Venda finalizada!");
    }//GEN-LAST:event_jButton1ActionPerformed

    // Remover Produto da lista
    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        int index = jTable1.getSelectedRow();

        if (index != -1) {
            Vector item = (Vector) modelo.getDataVector().get(index);

            double total = (double) item.get(4);
            totalVenda -= total;
            jTextField5.setText(totalVenda.toString());

            modelo.removeRow(index);
            modelo.fireTableRowsDeleted(index, index);
        } else {
            JOptionPane.showMessageDialog(this, "Para remover um produto da lista, clique nele para selecioná-lo e então aperte o botão 'Remover'");
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

