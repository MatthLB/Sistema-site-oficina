    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        int codigo = Integer.parseInt(jTextField1.getText());

        boolean encontrado = false;
        p = null;

        for (Object o : listaProdutos) {
            p = (Produto) o;

            if (p.getCodigo() == codigo) {
                encontrado = true;
                break;
            }
        }

        if (encontrado) {
            jTextField2.setText(p.getDescricao());
            Double valor = p.getValor();
            jTextField3.setText(valor.toString());

            jTextField4.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "O produto não foi encontrado");
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

    private void jTextField4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField4ActionPerformed
        int quantidade = Integer.parseInt(jTextField4.getText());

        if (p != null) {
            // Verificar se há a quantidade necessária no estoque
            if (quantidade > p.getQuantidadeEstoque()) {
                JOptionPane.showMessageDialog(this, "O estoque é menor que a quantidade requisitada!");
            } else {
                double totalProduto = quantidade * p.getValor();

                totalVenda += totalProduto;
                jTextField5.setText(Double.toString(totalVenda));

                modelo.addRow(new Object[]{p.getCodigo(), p.getDescricao(), p.getValor(), quantidade, totalProduto});
            }
        }
    }//GEN-LAST:event_jTextField4ActionPerformed

